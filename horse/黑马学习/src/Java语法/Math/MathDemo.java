package Java语法.Math;

public class MathDemo {
    public static void main(String[] args) {
        System.out.println(Math.abs(-2));//由于Math里的方法都是静态方法，所以可以直接用类调用
        System.out.println(Math.ceil(675464.3));//ceil天花板 的意思
        System.out.println(Math.floor(4342.6F));//地板
        System.out.println(Math.max(453.2F,32));
        System.out.println(Math.pow(4.0,4));

    }
}
